//NAV BAR
const openBurger = document.getElementById("navbar");
const closeBurger = document.getElementById("navbar");

function openNav() {
  openBurger.style.width = "100%";
  openBurger.style.backgroundColor = "rgba(0,0,0,1)";
}

function closeNav() {
  closeBurger.style.width = "0";
  closeBurger.style.backgroundColor = "transparent";
}
